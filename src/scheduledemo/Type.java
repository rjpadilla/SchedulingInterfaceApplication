package scheduledemo;


public class Type {

    private String count;
    private String type;
    private String month;

    public Type(String count, String type, String month) {

        this.count = count;
        this.type = type;
        this.month = month;
    

    }

    public String getCount() {
        return count;
    }

    public String getType() {
        return type;
    }

    public String getMonth() {
        return month;
    }

    

}
